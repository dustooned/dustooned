async function downloadBuffer(url) {
  const res = await fetch(url);
  if (!res.ok) throw new Error(`HTTP ${res.status} for ${url}`);
  return Buffer.from(await res.arrayBuffer());
}

for (const [slug, suffixes] of Object.entries(galleries)) {
  const outDir = path.join(OUT_DIR, slug, "storyboards");
  await mkdir(outDir, { recursive: true });

  let i = 1;
  for (const suffix of suffixes) {
    const url = PREFIX + suffix;
    const outPath = path.join(outDir, `storyboard-${String(i).padStart(3, "0")}.webp`);
    try {
      const buf = await downloadBuffer(url);
      await sharp(buf)
        .resize({ width: MAX_EDGE, height: MAX_EDGE, fit: "inside", withoutEnlargement: true })
        .webp({ quality: 80 })
        .toFile(outPath);
    } catch (err) {
      console.error(`FAILED ${slug} #${i}: ${err.message}`);
    }
    i += 1;
  }
  console.log(`${slug}: ${suffixes.length} images done`);
}

console.log("Done.");
